nome = input('Escreva o nome do Aluno: ')
notaUm = float (input('Informe a primeira nota do Aluno: '))
notaDois = float (input('Informe a segunda nota do Aluno: '))
notaTres = float (input('Informe a terceira nota do Aluno: '))
media = ((notaUm + notaDois + notaTres)/3)

print ('O nome do Aluno é: ', nome,"\n")

print ('A Primeira Nota é: ', int(notaUm),"\n")

print ('A Segunda Nota é: ', int(notaDois),"\n")

print ('A Terceira Nota é: ', int(notaTres),"\n")

print ('A Média é: ', int((media)),"\n")
